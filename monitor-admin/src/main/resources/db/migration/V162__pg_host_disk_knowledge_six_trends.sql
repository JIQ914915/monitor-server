<h2>四、关联指标趋势判读</h2>
<p>事件分析页固定展示六个互补指标，排查时应结合趋势相关性判断空间来源：</p>
<ul>
  <li><strong>磁盘使用率（最高挂载点）：</strong>告警主指标。持续线性上升通常是容量增长；短时间陡升优先排查批量任务、日志、临时文件或 WAL 堆积。</li>
  <li><strong>磁盘 IO 繁忙度：</strong>判断磁盘是否同时存在性能瓶颈。使用率高但 IO 平稳偏向容量问题；使用率与 IO 同时升高，需排查批量读写、检查点刷盘、VACUUM、备份等任务。</li>
  <li><strong>实例总容量：</strong>小时级采集。它与磁盘使用率同步增长时，业务数据或表索引膨胀的可能性较高；两者走势不一致时，重点检查 WAL、日志、归档、备份及主机其他目录。</li>
  <li><strong>WAL 生成速率：</strong>突增通常对应批量写入、大事务、全页写增加或高频更新；结合归档状态和复制消费速度判断是否会形成 WAL 堆积。</li>
  <li><strong>临时文件写入速率：</strong>持续升高说明排序、哈希、聚合或物化操作频繁落盘，优先通过 Top SQL 和 <code>log_temp_files</code> 定位语句。</li>
  <li><strong>复制槽滞留 WAL 最大值：</strong>持续升高且槽位 inactive 时，说明消费者停滞导致 WAL 无法回收。删除槽位前必须确认消费者已永久下线并评估重建成本。</li>
</ul>
<h3>无数据的正常与异常场景</h3>
<ul>
  <li>未配置复制槽时，“复制槽滞留 WAL 最大值”无数据属于正常现象。</li>
  <li>PostgreSQL 13 不提供 <code>pg_stat_wal</code>，因此“WAL 生成速率”可能无数据；可结合归档统计、实例写入量和主机目录占用判断。</li>
  <li>实例总容量按小时采集，告警前后 30 分钟内可能没有采样点；事件页会自动扩展为告警前后 12 小时查询。</li>
  <li>磁盘使用率或磁盘 IO 繁忙度无数据通常不正常，应检查实例是否关联主机、exporter 是否启用文件系统/磁盘采集器，以及主机指标是否成功扇出到该实例。</li>
</ul>
<h3>推荐判读组合</h3>
<ol>
  <li>磁盘使用率上升 + 实例总容量上升：排查业务数据增长、索引增长和表膨胀。</li>
  <li>磁盘使用率上升 + WAL/复制槽滞留上升：排查归档失败、复制中断或失效复制槽。</li>
  <li>磁盘使用率上升 + 临时文件写入升高：定位大排序、哈希、报表和批处理 SQL。</li>
  <li>磁盘使用率上升，但 PG 四项指标平稳：按目录排查数据库日志、备份、归档副本和其他主机文件。</li>
</ol>
